// Relational and Equality Operators
// Relational
// > - more than
// < -  less than
// >= - more than or equal
// <= - less than or equal

var result = 6 >= 5
console.log(result)

// Equality

var x = 1
console.log(x == '1') // True, lose comparison
console.log(x === '1') // False, strict comparison. x = a number value, not string
console.log(x === 1) // True